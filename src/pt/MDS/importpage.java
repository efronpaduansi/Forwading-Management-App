     private void jobNo(){
         try{
                Connection conn = DriverManager.getConnection("jdbc:mysql://localhost/pt_mds", "root", "");
                Statement stat = conn.createStatement();
                ResultSet rs = stat.executeQuery("select max(right(job_no,3)) as no FROM tb_import ");

                    while(rs.next())
                    {
                            
                        if(rs.first() == false)
                        {
                            
                            txtJobNo.setText("MDS2021I-001");

                        }
                        else
                            {
                                rs.last();
                                int auto_id = rs.getInt(1) + 1;
                                String no = String.valueOf(auto_id);
                                int noLong = no.length();
                                //MENGATUR jumlah 0
                                 for(int a=0;a<3-noLong;a++)
                                 {
                                     no = "0" + no;
                                 }
                                 
                                 txtJobNo.setText("MDS-2021I" + no);

                            }       
                    }    
                }catch (Exception e)
                {
                    JOptionPane.showMessageDialog(null, e);
                }
    }
    
