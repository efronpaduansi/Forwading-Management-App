    private void mnSaveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_mnSaveMouseClicked
        // Input data ke table import
        
        //Atur tampilan tangggal
        String tampilan = "dd-MM-yyyy";
        SimpleDateFormat fm = new SimpleDateFormat(tampilan);
        String tanggalETD = String.valueOf(fm.format(tglETD.getDate()));
        String tanggalETA = String.valueOf(fm.format(tglETA.getDate()));
        
        try
        {
            //validasi form
            if(txtJobNo.getText().equals(""))
            {
               JOptionPane.showMessageDialog(this, "Job No tidak boleh kosong!", "Warning", JOptionPane.WARNING_MESSAGE);  
            }
            else if(txtBL.getText().equals(""))
            {
                 JOptionPane.showMessageDialog(this, "BL tidak boleh kosong!", "Warning", JOptionPane.WARNING_MESSAGE);
            }
            else
            {
                
            
            //Buat query ke database
            String sql = "INSERT INTO tb_import VALUES("
                    + "'"+txtJobNo.getText()+"', '"+txtBL.getText()+"', '"+txtShipper.getText()+"', '"+txtShipperName.getText()+"', '"+txtShipperAdd.getText()+"', "
                    + "'"+txtConsignee.getText()+"', '"+txtConsigneeName.getText()+"', '"+txtConsigneeAddress.getText()+"', '"+txtMarketing.getText()+"', "
                    + "'"+txtVessel.getText()+"', '"+txtPOL.getText()+"', '"+txtPlaceDlv.getText()+"', '"+tanggalETD+"', '"+rboCY.isSelected()+"',"
                    + " '"+rboCFS.isSelected()+"', '"+txtGrossW.getText()+"', '"+txtCo20.getText()+"', '"+txtCo40.getText()+"', '"+txtCo45.getText()+"', '"+txtAgenCd.getText()+"', "
                    + "'"+txtVoyage.getText()+"', '"+txtPOD.getText()+"', '"+tanggalETA+"', '"+txtNetW.getText()+"', '"+txtPackages.getText()+"')";
            
            //Eksekusi query
            java.sql.Connection conn = (Connection)koneksi.koneksi();
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            
            // eksekusi query dan simpan hasilnya di obj ResultSet
            pst.execute();
            
            //Pesan informasi berhasil
            JOptionPane.showMessageDialog(null, "Data Berhasil di Simpan", "Information", JOptionPane.INFORMATION_MESSAGE);
            }
            int jawab = JOptionPane.showOptionDialog(this, 
                    "Apakah anda ingin menambahkan data lagi?", 
                    "Confirm", 
                    JOptionPane.YES_NO_OPTION, 
                    JOptionPane.QUESTION_MESSAGE, null, null, null);
    
              if(jawab == JOptionPane.YES_OPTION)
                {  
                    clear();
                }
        }
        catch(Exception e)
        {
//            JOptionPane.showMessageDialog(this, e.getMessage());
            JOptionPane.showMessageDialog(null, "Data Gagal di Simpan!", "Error", JOptionPane.ERROR_MESSAGE);
        }
        jobNo();
        bookingNumber();
        
    }//GEN-LAST:event_mnSaveMouseClicked

