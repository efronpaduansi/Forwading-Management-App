    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        txtName.setEnabled(true);
        txtUsername.setEnabled(true);
        txtLevel.setVisible(false);
        cbbLevel.setVisible(true);
        btnDelete.setEnabled(false);
        
        //MENGUPDATE DATA DI DALAM DATABASE
        IdMember = String.valueOf(txtIdMember.getText());
        name = String.valueOf(txtUsername.getText());
        usrname = String.valueOf(txtUsername.getText());
        lvl = String.valueOf(cbbLevel.getSelectedItem());
        
          if (JOptionPane.showConfirmDialog(this,"Anda yakin mengubah data ini?","konfirmasi", JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE)== JOptionPane.YES_OPTION) 
          {
              //Update data
              try{
                  String sql = "UPDATE employee SET idmember = '"+IdMember+"', fullname = '"+name+"', username = '"+usrname+"', level = '"+lvl+"' WHERE idmember = '"+txtIdMember.getText()+"' ";
                  java.sql.Connection conn=(Connection)koneksi.koneksi();
                  java.sql.PreparedStatement pst=conn.prepareStatement(sql);
                  pst.execute(sql);
                  
                    JOptionPane.showMessageDialog(null, "Data berhasil di edit");
                  
              } catch(Exception e){
                   JOptionPane.showMessageDialog(null, "Data gagal di ubah!"+e.getMessage());
                  
              }
          
          
          
          }
          
     
        
    }//GEN-LAST:event_btnEditActionPerformed

