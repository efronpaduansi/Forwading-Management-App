    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
       
         try{
           
            String sql = "DELETE FROM employee WHERE idmember = '"+txtIdMember.getText()+"'";
            java.sql.Connection conn=(Connection)koneksi.koneksi();
            java.sql.PreparedStatement pst=conn.prepareStatement(sql);
           
           
             int jawab = JOptionPane.showOptionDialog(this, 
                    "Kamu yakin menghapus data ini? Data Karyawan yang telah di hapus tidak bisa login di system", 
                    "Confirm", 
                    JOptionPane.YES_NO_OPTION, 
                    JOptionPane.QUESTION_MESSAGE, null, null, null);
    
              if(jawab == JOptionPane.YES_OPTION)
                {
                    pst.execute();
                    JOptionPane.showMessageDialog(this, "Data berhasil di hapus");
                        cbbLevel.setVisible(false);
                        label1.setVisible(false);
                        label2.setVisible(false);
                        label3.setVisible(false);
                        label4.setVisible(false);
                        txtIdMember.setVisible(false);
                        txtName.setVisible(false);
                        txtUsername.setVisible(false);
                        txtLevel.setVisible(false);
            
                 }
    
       } catch(Exception e)
       {
           JOptionPane.showMessageDialog(this, e.getMessage());
       }
           load_table();
        
        
        
        
    }//GEN-LAST:event_btnDeleteActionPerformed

