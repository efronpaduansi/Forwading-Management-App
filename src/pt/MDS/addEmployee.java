        private void IDMember(){
            try{
                Connection conn = DriverManager.getConnection("jdbc:mysql://localhost/pt_mds", "root", "");
                Statement stat = conn.createStatement();
                ResultSet rs = stat.executeQuery("select max(right(idmember,3)) as no FROM employee ");

                    while(rs.next())
                    { if(rs.first() == false)
                        {      
                            idmember.setText("202102001");
                        }
                        else {
                               rs.last();
                               int auto_id = rs.getInt(1) + 1;
                               String no = String.valueOf(auto_id);
                               int noLong = no.length();
                                //MENGATUR jumlah 0
                                 for(int a=0;a<3-noLong;a++)
                                 { no = "0" + no;} 
                                 idmember.setText("202102" + no);
                            }       
                    }    
                }catch (Exception e)
                {
                    JOptionPane.showMessageDialog(null, e);
                }
            }
        
