    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        //Menentukan panjang password
        int pjgPassword = password1.getText().length();
         //Validasi semua form
          try{
                if(fullname.getText().equals("")){
                      JOptionPane.showMessageDialog(this, "Field tidak boleh kosong!", "Warning", JOptionPane.WARNING_MESSAGE);
                }
                else if(username.getText().toLowerCase().equals("")){
                      JOptionPane.showMessageDialog(this, "Field tidak boleh kosong!", "Warning", JOptionPane.WARNING_MESSAGE);
                }
                else if(password1.getText().equals("")){
                      JOptionPane.showMessageDialog(this, "Field tidak boleh kosong!", "Warning", JOptionPane.WARNING_MESSAGE);
                }
                else if(password2.getText().equals("")){
                      JOptionPane.showMessageDialog(this, "Field tidak boleh kosong!", "Warning", JOptionPane.WARNING_MESSAGE);
                     
                }
                else if(pjgPassword <8){
                      JOptionPane.showMessageDialog(this, "Register Gagal!. Panjang Password Minimal 8 Karakter!", "Warning", JOptionPane.WARNING_MESSAGE);
                }
                
                else{
                    
                     //Input ke dalam database
                    String sql="INSERT INTO employee VALUE('"+idmember.getText()+"', '"+fullname.getText()+"', '"+username.getText()+"', '"+password1.getText()+"', '"+password2.getText()+"', '"+cbbLevel.getSelectedItem()+"')";
                    java.sql.Connection conn=(Connection)koneksi.koneksi();
                    java.sql.PreparedStatement pst=conn.prepareStatement(sql);
                    pst.execute();
                    
                    JOptionPane.showMessageDialog(null, "Data berhasil ditambahkan");
                    fullname.setText("");
                    username.setText("");
                    password1.setText("");
                    password2.setText("");
                    cbbLevel.getSelectedItem().equals("--Select Level--");
                    jLabel1.setText("");
                    }
                
                } catch(Exception e)
                {
                    JOptionPane.showMessageDialog(this, e.getMessage()); 
              
                }
                IDMember();
        
        
    }//GEN-LAST:event_btnSubmitActionPerformed

