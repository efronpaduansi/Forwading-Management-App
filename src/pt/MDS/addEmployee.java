    private void password2KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_password2KeyReleased
       // Mengatur Konfirmasi password
         String Password1 = new String (password1.getText());
         String Password2 = new String (password2.getText());
         
         if(Password2.equals("")){
              jLabel1.setText("");
         }
         else if(Password1.equals(Password2)){
              jLabel1.setText("Password cocok");
              jLabel1.setForeground(Color.GREEN);
          }
          else {
              jLabel1.setText("Password tidak cocok");
              jLabel1.setForeground(Color.RED);
          }
    }//GEN-LAST:event_password2KeyReleased

